; first go thru and edit string to be in initial data value memory locations
; then as we go thru printing each data value to screen, just increase memory location value by 1
; once we get to the end of a line of the string, increment line number counter and add new line character
; after weve gone thru all 16 lines we end.

; R0 = current output to console
; R1 = current character data				xXX00		binary data
; R2 = current character data address		x3xxx		binary data address
; R3 = current character address			x500x		string character
; R4 = const start of datatable
; R5 = line number counter / math counter
; R6 = bit number counter
; R7 = unused (used by subroutines and trap functions)


			.ORIG			x3000
			AND				R5,R5,#0
			LD				R3,STRING
			LEA				R4,FONT_DATA
			LDR				R2,R3,#0
			BRz				SKIP
MODLOOP
			ADD				R5,R5,#4
			
	MATHLOOP
				ADD				R2,R2,R2
				ADD				R5,R5,#-1
				BRp				MATHLOOP
			ADD				R2,R2,R4
			STR				R2,R3,#0
			ADD				R3,R3,#1
			LDR				R2,R3,#0
			BRnp			MODLOOP
			
			
SKIP		LD				R4,STRING
			AND				R5,R5,#0
			ADD				R5,R5,#15


LINELOOP
			
			AND				R6,R6,#0
			ADD				R3,R4,#0
			LDR				R2,R3,#0
			BRz				SKIP2
			
	CHARACTERLOOP
				LDR				R1,R2,#0
				ADD				R6,R6,#8
		BITLOOP
					ADD				R1,R1,#0		;load the current line of data into cc
					BRzp			#1
					LDI				R0,FOREGROUND	;if its negative (msb is 1), set r0 to our foreground character
					
					ADD				R1,R1,#0
					BRn				#1
					LDI				R0,BACKGROUND	;if its positive or zero (msb is 0), set r0 to our background character
					
					OUT								;print out r0
					
					ADD				R1,R1,R1
					ADD				R6,R6,#-1
					BRp			BITLOOP
				
				
				
				ADD				R2,R2,#1
				STR				R2,R3,#0
				ADD				R3,R3,#1
				LDR				R2,R3,#0
				BRnp			CHARACTERLOOP
			
SKIP2		LD				R0,NEWLINE
			OUT
			ADD				R5,R5,#-1
			BRzp			LINELOOP
			
			
			HALT


NEWLINE		.FILL			x000A			;ascii code for a new line character
BACKGROUND	.FILL			x5000			;memory locations for our 3 inputs
FOREGROUND	.FILL			x5001
STRING		.FILL			x5002


